package com.dave.fantasyfootball.form;


public class SelectionFormPlayer {

	int id;
	boolean isStarter;
	String subRank;
	

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public boolean getIsStarter() {
		return isStarter;
	}

	public void setIsStarter(boolean isStarter) {
		this.isStarter = isStarter;
	}

	public String getSubRank() {
		return subRank;
	}

	public void setSubRank(String subRank) {
		this.subRank = subRank;
	}

}
